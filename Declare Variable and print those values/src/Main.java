import java.util.*;

public class Main {
    public static void main(String[] args)
    {
        int num = 5;
        char ch = 'A';
        float value = 6.23f;
        double d = 20.01d;
        long l = 345267L;
        boolean b = true;
        System.out.println("Int : " + num);
        System.out.println("Char : " + ch);
        System.out.println("Float : " + value);
        System.out.println("Double : " + d);
        System.out.println("Long : " + l);
        System.out.println("Boolean : " + b);
        System.out.println("Java does not have a datatype for unsigned integers and unsigned float");

    }
}
